#include "libft.h"

int	ft_atoi(const char *str)
{
	int				i;
	long long int	res;
	int				sign;
	long long int	temp;

	i = 0;
	res = 0;
	sign = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i++] == '-')
			sign *= -1;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		temp = res;
		res = res * 10 + (str[i++] - '0');
		if (temp > res && sign == 1)
			return (-1);
		if (temp > res && sign == -1)
			return (0);
	}
	return (res * sign);
}
