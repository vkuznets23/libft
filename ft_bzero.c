#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*ptr;
	size_t			i;

	i = 0;
	ptr = (unsigned char *)s;
	if (n != 0)
	{
		while (i < n)
		{
			ptr[i] = '\0';
			i++;
		}
	}
}
